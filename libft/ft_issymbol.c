#include"libft.h"
#include"stdio.h"

int	ft_issymbol(int c)
{
	if ((c >= ':' && c <= '@') || (c >= '[' && c <= '`')
		|| (c >= '{' && c <= '~'))
		return (1);
	return (0);
}
