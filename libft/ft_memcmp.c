#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t size)
{
	if (size == 0)
		return (0);
	while (size--)
	{
		if (*(unsigned char *)s1 != *(unsigned char *)s2)
			return (*(unsigned char *)s1 - *(unsigned char *)s2);
		s1++;
		s2++;
	}
	return (0);
}
